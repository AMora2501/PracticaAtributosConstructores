package ejercicio4;
public class Pelicula {
    
    //Variables
    String titulo;
    String genero;
    int duracionMinutos;
    double calificacion;
    
    //Constructor
    public Pelicula(String titulo, String genero, int duracionMinutos, double calificacion){
        this.titulo = titulo;
        this.genero = genero;
        this.duracionMinutos = duracionMinutos;
        this.calificacion = calificacion;
    }
}
